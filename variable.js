// Declarar uma variável e atribuir um valor
let nome = "Maria";
console.log(nome);  // Saída: Maria

// Modificar o valor de uma variável
nome = "João";
console.log(nome);  // Saída: João

// Declarar uma constante
const idade = 30;
console.log(idade);  // Saída: 30

// Tentar modificar o valor de uma constante resultará em um erro
idade = 35;  // Erro: Assignment to constant variable

// Declarar uma variável sem atribuir um valor inicial
let sobrenome;
console.log(sobrenome);  // Saída: undefined

// Atribuir um valor posteriormente
sobrenome = "Silva";
console.log(sobrenome);  // Saída: Silva

// Declarar múltiplas variáveis em uma única linha
let a = 10, b = 20, c = 30;
console.log(a, b, c);  // Saída: 10 20 30
